#!/usr/bin/env node

const fs = require("node:fs");
const path = require("node:path");
const { spawnSync } = require("node:child_process");

function usage() {
  console.log(`Usage:
  node --experimental-strip-types tests/eval-search/eval-search-propose-pr.ts --run-dir <dir> [options]

Options:
  --optimizer-mode <codex|draft-only>  default: codex
  --skip-pr                            run Optimizer and gates, but do not push/create PR
  --skip-gate                          skip make unit-test
  --base-ref <ref>                     default: origin/main
  --worktree-dir <dir>                 default: /tmp/larksuite-cli-eval-pr-<run-id>-<pid>

The script creates an isolated git worktree for Optimizer changes so local dirty
harness work is not mixed into search-effect PRs.`);
}

function parseArgs(argv) {
  const out: any = {
    runDir: "",
    optimizerMode: "codex",
    skipPr: false,
    skipGate: false,
    baseRef: "origin/main",
    worktreeDir: "",
  };
  for (let i = 0; i < argv.length; i += 1) {
    const arg = argv[i];
    const next = () => {
      if (i + 1 >= argv.length) {
        throw new Error(`missing value for ${arg}`);
      }
      i += 1;
      return argv[i];
    };
    if (arg === "--help" || arg === "-h") {
      out.help = true;
    } else if (arg === "--run-dir") {
      out.runDir = next();
    } else if (arg === "--optimizer-mode") {
      out.optimizerMode = next();
    } else if (arg === "--skip-pr") {
      out.skipPr = true;
    } else if (arg === "--skip-gate") {
      out.skipGate = true;
    } else if (arg === "--base-ref") {
      out.baseRef = next();
    } else if (arg === "--worktree-dir") {
      out.worktreeDir = next();
    } else {
      throw new Error(`unknown option ${arg}`);
    }
  }
  if (!out.help && !out.runDir) {
    throw new Error("--run-dir is required");
  }
  if (!["codex", "draft-only"].includes(out.optimizerMode)) {
    throw new Error("--optimizer-mode must be codex or draft-only");
  }
  return out;
}

function repoRoot() {
  const result = spawnSync("git", ["rev-parse", "--show-toplevel"], {
    encoding: "utf8",
  });
  if (result.status !== 0) {
    throw new Error("must run inside a git worktree");
  }
  return result.stdout.trim();
}

function run(cmd, args, opts: any = {}) {
  const result = spawnSync(cmd, args, {
    cwd: opts.cwd,
    encoding: "utf8",
    maxBuffer: 128 * 1024 * 1024,
    timeout: opts.timeout || 30 * 60 * 1000,
  });
  return {
    cmd,
    args,
    status: result.status,
    stdout: result.stdout || "",
    stderr: result.stderr || "",
    ok: result.status === 0,
    error: result.error,
  };
}

function mustRun(cmd, args, opts: any = {}) {
  const result = run(cmd, args, opts);
  if (!result.ok) {
    throw new Error(
      `${[cmd, ...args].join(" ")} exited ${result.status}: ${result.stderr || result.stdout}`,
    );
  }
  return result;
}

function readJson(file, fallback = null) {
  if (!fs.existsSync(file)) {
    return fallback;
  }
  return JSON.parse(fs.readFileSync(file, "utf8"));
}

function writeJson(file, value) {
  fs.writeFileSync(file, `${JSON.stringify(value, null, 2)}\n`);
}

function ensureDir(dir) {
  fs.mkdirSync(dir, { recursive: true });
}

function safeRunId(runId) {
  return String(runId || "run").replace(/[^A-Za-z0-9_.-]+/g, "-");
}

function appendStatus(runDir, patch) {
  const file = path.join(runDir, "pr-draft", "status.json");
  const current = readJson(file, {});
  writeJson(file, { ...current, ...patch, updated_at: new Date().toISOString() });
}

function renderFindingList(findings) {
  if (!findings || findings.length === 0) {
    return "- No actionable findings.\n";
  }
  return findings
    .slice(0, 20)
    .map(
      (item) =>
        `- ${item.finding_id} [${item.priority}] ${item.bucket}/${item.target_domain} -> ${item.target_file}: ${item.suggestion} (cases: ${(item.driving_cases || []).join(", ")})`,
    )
    .join("\n");
}

function writeDraftFiles(runDir, summary) {
  const draftDir = path.join(runDir, "pr-draft");
  ensureDir(draftDir);
  const commitMessage = `docs(lark-search): improve search workflow from eval run ${summary.run_id}

Driven by /eval-search propose-pr ${summary.run_id}.

- Primary bottleneck: ${summary.primary_bottleneck || "n/a"}
- Findings: ${(summary.findings || []).length}
`;
  fs.writeFileSync(path.join(draftDir, "commit_message.txt"), commitMessage);
  const description = `<!-- Generated by /eval-search propose-pr run-id: ${summary.run_id} -->

## Eval Summary

- Run: ${summary.run_id}
- Dataset size: ${summary.dataset_size}
- Scored: ${summary.scored}
- Total: ${summary.totals?.sum}/${summary.totals?.max} (${summary.totals?.percent}%)
- Primary bottleneck: ${summary.primary_bottleneck || "n/a"}

## Findings

${renderFindingList(summary.findings)}

## Review Notes

This PR was generated from eval-search findings. Reviewers should check that
changes are domain-specific, generic, and not tailored to one case's exact gold
answer.
`;
  fs.writeFileSync(path.join(draftDir, "description.md"), description);
  fs.writeFileSync(
    path.join(draftDir, "unhandled_findings.md"),
    `# Unhandled findings\n\n${renderFindingList(summary.findings)}\n`,
  );
  writeJson(path.join(draftDir, "generalization_note.json"), {
    run_id: summary.run_id,
    principled_changes: [],
    case_specific_changes: [],
  });
}

function allowedDiffFile(file) {
  if (
    file.startsWith("skills/eval-search/") ||
    file.startsWith("tests/eval-search/") ||
    file === "AGENTS.md"
  ) {
    return false;
  }
  if (/^skills\/lark-[^/]+\/.*\.md$/.test(file)) {
    return true;
  }
  if (/^shortcuts\/[^/]+\/.*\.(go|md)$/.test(file)) {
    return true;
  }
  return false;
}

function createOptimizerPrompt(summary) {
  return `You are the Optimizer stage for eval-search.

Run id: ${summary.run_id}

Read these files in this worktree:
- tests/eval-search/runs/${summary.run_id}/summary.json
- tests/eval-search/runs/${summary.run_id}/verdicts.json

Task:
1. Pick one coherent, domain-specific improvement theme from the high/medium findings.
2. Edit only allowed production search assets:
   - skills/lark-*/**/*.md
   - shortcuts/<domain>/**/*.go or *.md
3. Do not edit skills/eval-search/**, tests/eval-search/**, AGENTS.md, generated run artifacts, or unrelated files.
4. Prefer generic workflow/strategy guidance over exact case answers.
5. If no safe production change exists, make no code changes and explain why in your final answer.

Top findings:
${renderFindingList(summary.findings)}
`;
}

function copyRunArtifacts(sourceRunDir, worktreeRoot, runId) {
  const dest = path.join(worktreeRoot, "tests/eval-search/runs", runId);
  ensureDir(path.dirname(dest));
  fs.cpSync(sourceRunDir, dest, { recursive: true });
  return dest;
}

function ensureCodex() {
  const result = run("codex", ["--version"]);
  if (!result.ok) {
    throw new Error("codex CLI is required for optimizer-mode=codex");
  }
}

function updateSummaryPr(runDir, patch) {
  const file = path.join(runDir, "summary.json");
  const summary = readJson(file, {});
  writeJson(file, { ...summary, ...patch });
}

function main() {
  const args = parseArgs(process.argv.slice(2));
  if (args.help) {
    usage();
    return;
  }

  const root = repoRoot();
  const runDir = path.isAbsolute(args.runDir) ? args.runDir : path.join(root, args.runDir);
  const summary = readJson(path.join(runDir, "summary.json"));
  if (!summary) {
    throw new Error(`summary.json not found in ${runDir}`);
  }
  if (summary.status !== "scored") {
    throw new Error(`summary status must be scored before propose-pr; got ${summary.status}`);
  }
  writeDraftFiles(runDir, summary);

  if (!summary.findings || summary.findings.length === 0) {
    appendStatus(runDir, { status: "no_pr", reason: "no actionable findings" });
    updateSummaryPr(runDir, { pr_status: "no_pr", pr_urls: [] });
    console.log(JSON.stringify({ status: "no_pr", reason: "no actionable findings" }, null, 2));
    return;
  }

  if (args.optimizerMode === "draft-only") {
    appendStatus(runDir, { status: "draft_only", reason: "optimizer-mode=draft-only" });
    updateSummaryPr(runDir, { pr_status: "draft_only", pr_urls: [] });
    console.log(JSON.stringify({ status: "draft_only", pr_draft: path.relative(root, path.join(runDir, "pr-draft")) }, null, 2));
    return;
  }

  ensureCodex();
  const runId = summary.run_id || path.basename(runDir);
  const safe = safeRunId(runId);
  const branch = `eval-search/auto-pr/${safe}-${process.pid}`;
  const worktreeDir =
    args.worktreeDir || path.join("/tmp", `larksuite-cli-eval-pr-${safe}-${process.pid}`);
  ensureDir(path.dirname(worktreeDir));
  mustRun("git", ["worktree", "add", "-b", branch, worktreeDir, args.baseRef], { cwd: root });
  const worktreeRunDir = copyRunArtifacts(runDir, worktreeDir, runId);
  const prompt = createOptimizerPrompt(summary);
  const draftDir = path.join(worktreeRunDir, "pr-draft");
  ensureDir(draftDir);
  fs.writeFileSync(path.join(draftDir, "optimizer-prompt.md"), prompt);
  appendStatus(runDir, { status: "optimizer_running", branch, worktree_dir: worktreeDir });

  const codexResult = run(
    "codex",
    [
      "exec",
      "-C",
      worktreeDir,
      "--sandbox",
      "workspace-write",
      "--ask-for-approval",
      "never",
      "--output-last-message",
      path.join(draftDir, "optimizer-output.txt"),
      prompt,
    ],
    { cwd: worktreeDir, timeout: 90 * 60 * 1000 },
  );
  if (!codexResult.ok) {
    appendStatus(runDir, {
      status: "optimizer_failed",
      branch,
      worktree_dir: worktreeDir,
      error: codexResult.stderr || codexResult.stdout,
    });
    throw new Error(`codex optimizer failed: ${codexResult.stderr || codexResult.stdout}`);
  }

  const diffFiles = mustRun("git", ["diff", "--name-only"], { cwd: worktreeDir }).stdout
    .split(/\r?\n/)
    .map((item) => item.trim())
    .filter(Boolean);
  fs.writeFileSync(path.join(draftDir, "touched_files.txt"), `${diffFiles.join("\n")}\n`);
  if (diffFiles.length === 0) {
    appendStatus(runDir, { status: "no_changes", branch, worktree_dir: worktreeDir });
    updateSummaryPr(runDir, { pr_status: "no_changes", pr_urls: [] });
    fs.cpSync(draftDir, path.join(runDir, "pr-draft"), { recursive: true });
    console.log(JSON.stringify({ status: "no_changes", branch, worktree_dir: worktreeDir }, null, 2));
    return;
  }
  const disallowed = diffFiles.filter((file) => !allowedDiffFile(file));
  if (disallowed.length > 0) {
    appendStatus(runDir, {
      status: "blocked_disallowed_paths",
      branch,
      worktree_dir: worktreeDir,
      disallowed_files: disallowed,
    });
    throw new Error(`optimizer touched disallowed files: ${disallowed.join(", ")}`);
  }

  const diff = mustRun("git", ["diff"], { cwd: worktreeDir }).stdout;
  fs.writeFileSync(path.join(draftDir, "diff.patch"), diff);
  if (!args.skipGate) {
    appendStatus(runDir, { status: "quality_gate_running", branch, worktree_dir: worktreeDir });
    mustRun("make", ["unit-test"], { cwd: worktreeDir, timeout: 90 * 60 * 1000 });
  }

  mustRun("git", ["add", ...diffFiles], { cwd: worktreeDir });
  mustRun("git", ["commit", "-F", path.join(draftDir, "commit_message.txt")], { cwd: worktreeDir });
  if (args.skipPr) {
    appendStatus(runDir, { status: "committed_skip_pr", branch, worktree_dir: worktreeDir });
    updateSummaryPr(runDir, { pr_status: "committed_skip_pr", pr_urls: [] });
    fs.cpSync(draftDir, path.join(runDir, "pr-draft"), { recursive: true });
    console.log(JSON.stringify({ status: "committed_skip_pr", branch, worktree_dir: worktreeDir }, null, 2));
    return;
  }

  mustRun("git", ["push", "-u", "origin", branch], { cwd: worktreeDir, timeout: 30 * 60 * 1000 });
  const title = `docs(lark-search): improve search workflow from eval run ${runId}`;
  const prResult = mustRun(
    "gh",
    ["pr", "create", "--draft", "--title", title, "--body-file", path.join(draftDir, "description.md")],
    { cwd: worktreeDir, timeout: 30 * 60 * 1000 },
  );
  const prUrl = prResult.stdout.trim().split(/\r?\n/).find((line) => line.startsWith("http")) || prResult.stdout.trim();
  fs.writeFileSync(path.join(draftDir, "pr-url.txt"), `${prUrl}\n`);
  fs.cpSync(draftDir, path.join(runDir, "pr-draft"), { recursive: true });
  appendStatus(runDir, { status: "pr_created", branch, worktree_dir: worktreeDir, pr_url: prUrl });
  updateSummaryPr(runDir, { pr_status: "pr_created", pr_urls: [prUrl] });
  console.log(JSON.stringify({ status: "pr_created", branch, worktree_dir: worktreeDir, pr_url: prUrl }, null, 2));
}

try {
  main();
} catch (err) {
  console.error(err.stack || err.message);
  process.exitCode = 1;
}
